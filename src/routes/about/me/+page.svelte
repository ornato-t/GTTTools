	<h2 class="font-bold text-lg mt-6">La velocità come obiettivo</h2>
	<p class="mt-2 mb-4 text-justify">Da pendolare mi trovo spesso a dover prendere rapidamente decisioni sul mio percorso.</p>
	<ul class="my-4 text-justify list-disc list-inside">
		<li>Attendere alla fermata giusta</li>
		<li>Scegliere tra un tram, la metro o magari un treno del Servizio Ferroviario Metropolitano</li>
		<li>Evitare di perdere tempo attendendo e muovermi a piedi</li>
	</ul>
	<p class="my-4 text-justify">
		Tutte queste scelte richiedono un rapido accesso a dati aggiornati sulla posizione dei mezzi. L'app mira a fornire tali dati agli utenti nel minor tempo possibile.
		<br />
		Per raggiungere tale obiettivo si usano tecnologie all'avanguardia quali il recente framework <a class="link" href="https://kit.svelte.dev/">SvelteKit</a> e l'efficiente
		database <a class="link" href="https://www.mongodb.com">MongoDB</a>, entrambi mirati a massimizzare prestazioni ed affidabilità.
	</p>
	<p class="my-4 text-justify">
		L'applicazione è ottimizzata per telefoni e dispositivi mobili e da qualsiasi pagina è sempre possibile accedere ad altre sezioni in modo facile e veloce con al massimo due
		click. Questo principio è la pietra di volta su cui ho costruito l'intero servizio, e che mi impegno a tenere valido in ogni aggiornamento.
	</p>

